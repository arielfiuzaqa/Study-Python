''' Calculadora de Área de um Retângulo:

Peça ao usuário que insira o comprimento e a largura de um retângulo.
Calcule e exiba a área do retângulo.

Solicitamos ao usuário que insira o comprimento e a largura do retângulo usando input. 
Os valores inseridos são convertidos em números de ponto flutuante usando float.

Calculamos a área do retângulo multiplicando o comprimento pela largura.

Exibimos a área calculada usando print. '''


# Solicitar ao usuário que insira o comprimento e a largura do retângulo
comprimento = float(input("Digite o comprimento do retângulo: "))
largura = float(input("Digite a largura do retângulo: "))

# Calcular a área do retângulo
area = comprimento * largura

# Exibir a área do retângulo
print(f"A área do retângulo é: {area}")









